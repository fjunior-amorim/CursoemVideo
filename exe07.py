# ==== antecesso e sucessor ====
nun = int(input('digite um numero: '))
s = nun + 1
a = nun - 1
print('o sucessor é: {} \n'
      'o seu antecessor é: {}'.format(s, a))

# ========== dobro,triplo e raiz quadrada =============
db = nun * 2
tp = nun * 3
rq = nun ** (1/2)
print('o dobro é: {} \n'
      'o tripo é: {} \n'
      'a raiz quadrada é: {:.2f}'.format(db, tp, rq))
